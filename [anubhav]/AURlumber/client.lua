setElementData(localPlayer, "lumberjackTreePos", false)

function drawTrees()
	if (getTeamName(getPlayerTeam(localPlayer)) ~= "Civilian Workers") then
		stopLumberJackJob()
		return false
	end
	dxDrawRectangle(screenW * 0.7508, screenH * 0.4514, screenW * 0.2414, screenH * 0.0972, tocolor(0, 0, 0, 175), false)
	if (treesLeftToCut == "") then
		dxDrawText("Approach the red blip", screenW * 0.7508, screenH * 0.4514, screenW * 0.9922, screenH * 0.5486, tocolor(255, 255, 255, 255), 1.00, "pricedown", "center", "center", false, false, false, false, false)
	else
		dxDrawText(tostring(treesLeftToCut).." trees left", screenW * 0.7508, screenH * 0.4514, screenW * 0.9922, screenH * 0.5486, tocolor(255, 255, 255, 255), 1.00, "pricedown", "center", "center", false, false, false, false, false)
	end
end

function onElementDataChange(dataName)
	if (dataName == "Occupation" and getElementData(localPlayer, dataName) == "Lumberjack") then
		if (getTeamName(getPlayerTeam(localPlayer)) == "Civilian Workers") then
			initLumberJackJob()
		else
			if (lumberJackRunning) then
				stopLumberJackJob()
			end
		end
	elseif (dataName == "Occupation" and lumberJackRunning) then
		stopLumberJackJob()
	end
end
addEventHandler("onClientElementDataChange", localPlayer, onElementDataChange, false)
addEvent("onClientPlayerTeamChange", true)
addEventHandler("onClientPlayerTeamChange", localPlayer,
	function()

		setTimer(onElementDataChange, 1000, 1, "Occupation")
	end
)

function hitColShape(el)
	if (getElementType(el) ~= "player" or not lumberJackRunning or el ~= localPlayer) then
		return false
	end

	if (colshape ~= source) then
		removeEventHandler("onClientColShapeHit", source, hitColShape)
		removeEventHandler("onClientColShapeLeave", source, leaveColShape)
		return false 
	end

	if (isElement(colshapeBlip)) then
		destroyElement(colshapeBlip)
		colshapeBlip = nil
	end

	for i, v in pairs(currentBlips) do
		if (isElement(v)) then
			destroyElement(v)
		end
	end
	currentBlips = {}
	treesLeftToCut = 0

	for i, v in pairs(currentTrees) do
		currentBlips[i] = createBlipAttachedTo(i, 0, 1)
		treesLeftToCut = treesLeftToCut + 1
		removeEventHandler("onClientObjectDamage", i, cutTree)
		addEventHandler("onClientObjectDamage", i, cutTree)
	end
end

function leaveColShape(el)
	if (getElementType(el) ~= "player" or not lumberJackRunning or el ~= localPlayer) then
		return false
	end

	if (colshape ~= source) then
		removeEventHandler("onClientColShapeHit", source, hitColShape)
		removeEventHandler("onClientColShapeLeave", source, leaveColShape)
		return false 
	end

	if (isElement(colshapeBlip)) then
		destroyElement(colshapeBlip)
		colshapeBlip = nil
	end

	for i, v in pairs(currentBlips) do
		if (isElement(v)) then
			destroyElement(v)
		end		

		if (isElement(i)) then
			removeEventHandler("onClientObjectDamage", i, cutTree)
		end
	end
	currentBlips = {}

	colshapeBlip = createBlipAttachedTo(colshape, 0, 3)
	treesLeftToCut = ""

end

function recountTrees()
	treesLeftToCut = 0

	for i, v in pairs(currentTrees) do
		if (isElement(i)) then
			treesLeftToCut = treesLeftToCut + 1
		end
	end

	if (treesLeftToCut == 0) then
		createLumber()
	end
end

function createLumber()
	for i, v in pairs(currentTrees) do
		if (isElement(i)) then
			destroyElement(i)
		end

		if (isElement(v)) then
			destroyElement(v)
		end
	end

	for i, v in pairs(currentBlips) do
		if (isElement(v)) then
			destroyElement(v)
		end
	end

	currentTrees = {}
	currentBlips = {}

	treesLeftToCut = ""
	local unit = exports.AURunits:getUnitOnlineMembers()
	local randomChose = math.random(1, #treePositions)
	for i, v in pairs(unit) do
		if (v ~= localPlayer and getElementData(v, "Occupation") == "Lumberjack" and getElementData(v, "lumberjackTreePos") ~= getElementData(localPlayer, "lumberjackTreePos")) then
			randomChose = getElementData(v, "lumberjackTreePos") or math.random(1, #treePositions)
			break
		end
	end
	if (randomChose == getElementData(localPlayer, "lumberjackTreePos")) then
		randomChose = math.random(1, #treePositions)
	end
	local random = treePositions[randomChose]
	while not random["colShape"] do
		random = treePositions[math.random(#treePositions)]
	end
	setElementData(localPlayer, "lumberjackTreePos", randomChose)

	if (isElement(colshape)) then
		destroyElement(colshape)
	end
	if (isElement(colshapeBlip)) then
		destroyElement(colshapeBlip)
	end

	local zoneName = getZoneName(random['colShape'][1], random['colShape'][2], 1, false)
	exports.NGCdxmsg:createNewDxMessage("Go to "..zoneName.." for you next assignment! It has been marked by a red blip!", 255, 255, 0)
	colshape = createColCircle(random['colShape'][1], random['colShape'][2], 100)
	colshapeBlip = createBlipAttachedTo(colshape, 0, 3)
	random['colShape'] = nil

	for i, v in pairs(random) do
		local obj = createObject(treeIDs[math.random(#treeIDs)], v[1], v[2], v[3])
		setObjectBreakable(obj, true)
		local mark = createMarker(v[1]+0.5, v[2], v[3], "cylinder", 5, 255, 255, 0, 50)
		attachElements(mark, obj, 0, 0, -1)
		currentTrees[obj] = mark
	end

	addEventHandler("onClientColShapeHit", colshape, hitColShape)
	addEventHandler("onClientColShapeLeave", colshape, leaveColShape)
end

function cutTreeAnim(element)
	if (not currentTrees[element]) then
		return false 
	end
	setElementCollisionsEnabled(element, false)

	local x, y, z = getElementPosition(element)
	moveObject(element, 4000, x, y, z-10, 0, 90, 90)

	setTimer(
		function(el)
			if (isElement(currentTrees[el])) then
				destroyElement(currentTrees[el])
			end

			if (isElement(currentBlips[el])) then
				destroyElement(currentBlips[el])
			end

			currentBlips[el] = nil
			currentTrees[el] = nil

			destroyElement(el)

			recountTrees()
		end
	, 5000, 1, element)
end

function cutTree(_, elHit)
	if (not elHit or not isElement(elHit)) then
		return false
	end

	if (getElementType(elHit) ~= "player") then
		return false 
	end

	if (not currentTrees[source]) then
		return false
	end

	if (getPedWeapon(elHit) ~= 9) then
		return false 
	end

	if (elHit ~= localPlayer) then
		return false
	end

	if (treeDamage[source]) then
		if (getTickCount() - treeDamage[source][1] > 500) then
			treeDamage[source] = {getTickCount(), treeDamage[source][2] + 1}

			if (treeDamage[source][2] == 4) then
				cutTreeAnim(source)
				triggerServerEvent("payLumberJack", resourceRoot)
			end
		end
	else
		treeDamage[source] = {getTickCount(), 0}
	end
end

function initLumberJackJob()
	if (not lumberJackRunning) then
		lumberJackRunning = true
		createLumber()
		addEventHandler("onClientRender", root, drawTrees)
    end
end

function removeLumber()
	for i, v in pairs(currentTrees) do
		if (isElement(i)) then
			removeEventHandler("onClientObjectDamage", i, cutTree)
			destroyElement(i)
		end

		if (isElement(v)) then
			destroyElement(v)
		end
	end

	for i, v in pairs(currentBlips) do
		if (isElement(v)) then
			destroyElement(v)
		end
	end

	currentTrees = {}
	currentBlips = {}

	if (isElement(colshape)) then
		removeEventHandler("onClientColShapeHit", colshape, hitColShape)
		removeEventHandler("onClientColShapeLeave", colshape, leaveColShape)
		destroyElement(colshape)
	end

	if (isElement(colshapeBlip)) then
		destroyElement(colshapeBlip)
	end

	colshape = nil
	colshapeBlip = nil
end

function stopLumberJackJob()
    if (lumberJackRunning) then
	   lumberJackRunning = false
	   removeLumber()
	   removeEventHandler("onClientRender", root, drawTrees)
	   exports.CSGranks:closePanel()
    end
end

function loadOnStart()
	if (getElementData(localPlayer, "Occupation") == "Lumberjack" and getPlayerTeam(localPlayer) and getTeamName(getPlayerTeam(localPlayer)) == "Civilian Workers") then
		initLumberJackJob()
	end
end
addEventHandler("onClientResourceStart", resourceRoot, loadOnStart)

